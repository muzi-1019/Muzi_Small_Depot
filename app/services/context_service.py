def _cache_get(key: str) -> dict | None:
    with _cache_lock:
        if key in _cache:
            expire_at, data = _cache[key]
            if _time.time() < expire_at:
                return data
            del _cache[key]
    return None


def _cache_set(key: str, data: dict) -> None:
    with _cache_lock:
        _cache[key] = (_time.time() + _CACHE_TTL, data)


class ContextService:
    """实时上下文服务：获取用户的时间、地点和天气信息"""

    @staticmethod
    def get_realtime_context(
        client_ip: str | None = None,
        latitude: float | None = None,
        longitude: float | None = None,
    ) -> str:
        """
        获取实时上下文字符串（**尽量不阻塞、绝不抛异常**）。
        - 有缓存 → 立即返回时间 + 地点 + 天气
        - 无缓存 → 同步获取一次（国内 API 很快，通常 <2s），然后缓存
        """
        try:
            geo_key = f"geo_{client_ip or '__server__'}"
            geo = _cache_get(geo_key)
            if geo is None:
                # 首次：同步获取（国内 API 通常 <1s，可接受）
                geo = ContextService._fetch_geo(client_ip)
                if geo:
                    _cache_set(geo_key, geo)
                    ContextService._fetch_and_cache_weather(geo)
                else:
                    geo = {}

            time_str = ContextService._format_time()
            location_str = ContextService._format_location(geo)

            weather_str = ""
            weather_key = ContextService._weather_key(geo)
            if weather_key:
                wd = _cache_get(weather_key)
                weather_str = wd.get("text", "") if wd else ""

            parts = [f"当前时间：{time_str}"]
            if location_str:
                parts.append(f"用户所在地：{location_str}")
            if weather_str:
                parts.append(f"当地天气：{weather_str}")
            return "\n".join(parts)
        except Exception as e:
            logger.warning("获取实时上下文失败: %s", e)
            return f"当前时间：{ContextService._format_time()}"

    # ==================== 后台异步获取 ====================

    @staticmethod
    def _async_fetch_all(client_ip: str | None) -> None:
        """后台线程：IP 定位 → 查天气代码 → 查天气，全部异步完成"""
        key = f"geo_{client_ip or '__server__'}"
        with _pending_lock:
            if key in _pending:
                return
            _pending.add(key)

        def _worker():
            try:
                geo = ContextService._fetch_geo(client_ip)
                if geo:
                    _cache_set(key, geo)
                    ContextService._fetch_and_cache_weather(geo)
            except Exception as e:
                logger.warning("后台获取上下文失败: %s", e)
            finally:
                with _pending_lock:
                    _pending.discard(key)

        threading.Thread(target=_worker, daemon=True).start()

    @staticmethod
    def _weather_key(geo: dict) -> str:
        district = geo.get("district", "").replace("区", "").replace("县", "")
        city = geo.get("city", "")
        return f"weather_{district or city}" if (district or city) else ""

    # ==================== 同步网络请求（仅后台线程调用） ====================

    @staticmethod
    def _fetch_geo(client_ip: str | None) -> dict:
        """通过太平洋网络 IP 定位 API 获取地理位置"""
        local_ips = {"127.0.0.1", "::1", "localhost", "0.0.0.0"}
        is_local = (
            (not client_ip)
            or (client_ip in local_ips)
            or client_ip.startswith(("192.168.", "10.", "172."))
        )
        try:
            if is_local:
                url = "https://whois.pconline.com.cn/ipJson.jsp?json=true"
            else:
                url = f"https://whois.pconline.com.cn/ipJson.jsp?ip={client_ip}&json=true"
            with httpx.Client(timeout=5.0, trust_env=False) as client:
                resp = client.get(url, headers={"User-Agent": "Mozilla/5.0"})
                resp.raise_for_status()
                text = resp.content.decode("gbk", errors="ignore")
                data = _json.loads(text.strip())
            if data.get("err", "") == "":
                city_raw = data.get("city", "").replace("市", "")
                region = data.get("region", "")
                pro = data.get("pro", "").replace("省", "").replace("市", "")
                return {
                    "ip": data.get("ip", client_ip or ""),
                    "province": pro,
                    "city": city_raw or pro,
                    "district": region,
                    "addr": data.get("addr", ""),
                }
            logger.warning("pconline IP 定位失败: %s", data)
        except Exception as e:
            logger.warning("获取地理位置失败: %s", e)
        return {}

    @staticmethod
    def _fetch_and_cache_weather(geo: dict) -> None:
        """通过国内天气 API 获取天气并写入缓存"""
        district = geo.get("district", "").replace("区", "").replace("县", "")
        city = geo.get("city", "")
        # 先用区县名查城市代码，再用城市名兜底
        city_code = ContextService._get_weather_code(district) or ContextService._get_weather_code(city)
        if not city_code:
            logger.warning("未找到天气代码: district=%s city=%s", district, city)
            return
        cache_key = f"weather_{district or city}"
        try:
            url = f"http://t.weather.itboy.net/api/weather/city/{city_code}"
            with httpx.Client(timeout=8.0, trust_env=False) as client:
                resp = client.get(url)
                resp.raise_for_status()
                data = resp.json()
            if data.get("status") != 200:
                logger.warning("天气 API 返回异常: %s", data.get("message", ""))
                return
            wd = data.get("data", {})
            forecast = wd.get("forecast", [{}])[0]
            parts = []
            weather_type = forecast.get("type", "")
            if weather_type:
                parts.append(weather_type)
            wendu = wd.get("wendu")
            if wendu:
                parts.append(f"气温{wendu}°C")
            high = forecast.get("high", "").replace("高温 ", "")
            low = forecast.get("low", "").replace("低温 ", "")
            if high and low:
                parts.append(f"{low}~{high}")
            ganmao = forecast.get("fl", "")
            fx = forecast.get("fx", "")
            if fx and ganmao:
                parts.append(f"{fx}{ganmao}")
            shidu = wd.get("shidu", "")
            if shidu:
                parts.append(f"湿度{shidu}")
            quality = wd.get("quality", "")
            if quality:
                parts.append(f"空气{quality}")
            text = "，".join(parts)
            if text:
                _cache_set(cache_key, {"text": text})
        except Exception as e:
            logger.warning("获取天气信息失败: %s", e)

    @staticmethod
    def _get_weather_code(name: str) -> str | None:
        """从映射表查找天气城市代码"""
        if not name:
            return None
        clean = name.replace("市", "").replace("区", "").replace("县", "")
        if clean in _WEATHER_CODES:
            return _WEATHER_CODES[clean]
        for key, code in _WEATHER_CODES.items():
            if key in clean or clean in key:
                return code
        return None

    # ==================== 纯本地计算 ====================

    @staticmethod
    def _format_time() -> str:
        now = datetime.now()
        weekdays = ["星期一", "星期二", "星期三", "星期四", "星期五", "星期六", "星期日"]
        return now.strftime(f"%Y年%m月%d日 {weekdays[now.weekday()]} %H:%M")

    @staticmethod
    def _format_location(geo: dict) -> str:
        if not geo:
            return ""
        parts = []
        province = geo.get("province", "")
        city = geo.get("city", "")
        district = geo.get("district", "")
        if province:
            parts.append(province)
        if city and city != province:
            parts.append(city)
        if district:
            parts.append(district)
        return "".join(parts) if parts else ""
